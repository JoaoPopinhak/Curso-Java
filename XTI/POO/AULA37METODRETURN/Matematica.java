package POO.AULA37METODRETURN;

public class Matematica {
	
	/**
	 * 
	 * @param num1
	 * @param num2
	 * @return o maior dos dois numeros
	 */
	
	
	public int maior(int num1, int num2){
		if(num1>num2){
			return num1;
		}else {
			return num2;
		}
	}
	
	
	public int soma (int num1, int num2) {
		return num1+num2;
	}
}
